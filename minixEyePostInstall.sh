# ─────────────────────────────────────────────
# SECTION 3 — eMMC PRESERVATION
# Minimises write amplification on the internal eMMC flash:
#   3a  Disable swap (swapoff, remove /swapfile, purge fstab entry)
#   3b  Add noatime,nodiratime to root filesystem mount options
#   3c  Mount /tmp and /var/tmp as tmpfs (RAM-backed, no flash writes)
#   3d  Configure systemd journal as volatile with a 32 MB cap
#   3e  Aggressive log rotation: daily, 3 generations, compressed
#   3f  Suppress core dumps
#   3g  Tune kernel write-back to batch dirty pages (fewer flush cycles)
#   3h  Disable apt periodic timer (daily index refreshes churn flash)
# ─────────────────────────────────────────────
configure_emmc() {
    echo ""
    echo "=== SECTION 3: eMMC Preservation ==="

    # Take a single snapshot of fstab; all 3a/3b/3c edits validate against it.
    fstab_begin

    # ── 3a: Disable swap ────────────────────────────────────────────────────
    echo "  [3a] Disabling swap..."
    debug "Running: swapoff -a"
    swapoff -a 2>/dev/null || true

    if grep -q '/swapfile' /etc/fstab; then
        debug "Removing /swapfile entry from /etc/fstab"
        sed -i '/\/swapfile/d' /etc/fstab
        echo "       /swapfile removed from fstab."
    fi

    if [[ -f /swapfile ]]; then
        if confirm "       Delete /swapfile from disk?"; then
            debug "Deleting /swapfile"
            rm -f /swapfile
            echo "       /swapfile deleted."
        fi
    fi

    debug "Setting vm.swappiness=0"
    echo "vm.swappiness=0" > /etc/sysctl.d/99-swappiness.conf
    sysctl -w vm.swappiness=0 >/dev/null

    # ── 3b: noatime on root filesystem ──────────────────────────────────────
    echo "  [3b] Adding noatime,nodiratime to root mount..."
    if grep -E '^\S+\s+/\s' /etc/fstab | grep -q 'noatime'; then
        echo "       noatime already present — skipping."
    else
        debug "Patching root mount options via awk"
        # Write to /run (always tmpfs) — avoids any chance of clobber on /tmp
        # before /tmp's tmpfs mount is active.
        local tmpf
        tmpf="$(mktemp /run/fstab.XXXXXX 2>/dev/null || mktemp)"
        awk '$2=="/" && !/noatime/ {
            $4 = $4 ",noatime,nodiratime"
        } {print}' /etc/fstab > "$tmpf"
        mv "$tmpf" /etc/fstab
        echo "       noatime,nodiratime added to root mount."
    fi

    # ── 3c: tmpfs for /tmp and /var/tmp ─────────────────────────────────────
    echo "  [3c] Adding tmpfs mounts for /tmp and /var/tmp..."
    if ! grep -qE 'tmpfs\s+/tmp\s' /etc/fstab; then
        echo "tmpfs  /tmp      tmpfs  defaults,nosuid,nodev,noatime,size=128M  0  0" >> /etc/fstab
        echo "       tmpfs /tmp  added to fstab."
    else
        echo "       tmpfs /tmp  already present — skipping."
    fi

    if ! grep -qE 'tmpfs\s+/var/tmp\s' /etc/fstab; then
        echo "tmpfs  /var/tmp  tmpfs  defaults,nosuid,nodev,noatime,size=64M   0  0" >> /etc/fstab
        echo "       tmpfs /var/tmp  added to fstab."
    else
        echo "       tmpfs /var/tmp  already present — skipping."
    fi

    # Validate everything we just wrote; roll back on failure.
    fstab_validate_or_rollback "Section 3 (swap/noatime/tmpfs)"
    echo "       fstab validated."

    # ── 3d: Volatile systemd journal, 32 MB cap ──────────────────────────────
    echo "  [3d] Configuring systemd journal (volatile, 32 MB)..."
    local jdir="/etc/systemd/journald.conf.d"
    debug "Creating $jdir"
    mkdir -p "$jdir"
    cat > "$jdir/99-emmc.conf" <<'EOF'
[Journal]
Storage=volatile
RuntimeMaxUse=32M
Compress=yes
EOF
    debug "Restarting systemd-journald"
    systemctl restart systemd-journald
    echo "       journald set to volatile storage."

    # ── 3e: Aggressive log rotation ─────────────────────────────────────────
    echo "  [3e] Configuring logrotate (daily, 3 generations, compressed)..."
    cat > /etc/logrotate.d/99-emmc <<'EOF'
/var/log/*.log /var/log/**/*.log {
    daily
    rotate 3
    compress
    delaycompress
    missingok
    notifempty
    copytruncate
}
EOF
    echo "       logrotate override written."

    # ── 3f: Suppress core dumps ─────────────────────────────────────────────
    echo "  [3f] Disabling core dumps..."
    echo "kernel.core_pattern=/dev/null" > /etc/sysctl.d/99-no-coredumps.conf
    sysctl -w kernel.core_pattern=/dev/null >/dev/null
    local cddir="/etc/systemd/coredump.conf.d"
    mkdir -p "$cddir"
    cat > "$cddir/99-disable.conf" <<'EOF'
[Coredump]
Storage=none
ProcessSizeMax=0
EOF
    echo "       Core dumps suppressed."

    # ── 3g: Kernel write-back tuning ────────────────────────────────────────
    echo "  [3g] Tuning kernel write-back parameters..."
    cat > /etc/sysctl.d/99-emmc-writeback.conf <<'EOF'
# Batch dirty pages longer before flushing to reduce flash write cycles
vm.dirty_writeback_centisecs = 1500
vm.dirty_expire_centisecs    = 3000
vm.dirty_ratio               = 40
vm.dirty_background_ratio    = 10
EOF
    sysctl -p /etc/sysctl.d/99-emmc-writeback.conf >/dev/null
    echo "       Write-back tuning applied."

    # ── 3h: Disable apt periodic timers ─────────────────────────────────────
    echo "  [3h] Disabling apt periodic timers..."
    for timer in apt-daily.timer apt-daily-upgrade.timer; do
        if systemctl list-unit-files 2>/dev/null | grep -q "^${timer}"; then
            debug "Disabling $timer"
            systemctl disable "$timer" 2>/dev/null || true
            systemctl stop    "$timer" 2>/dev/null || true
            echo "       $timer disabled."
        fi
    done

    echo "[emmc] eMMC preservation complete."
}

