# SECTION 4 — AUDIO: PipeWire buffer config
# Creates /etc/pipewire/buffer.conf with pulse quantum/fragment overrides
# to reduce audio buffering artifacts.
# ─────────────────────────────────────────────
configure_audio() {
    echo ""
    echo "=== SECTION 4: Audio — PipeWire Buffer Config ==="

    local dir="/etc/pipewire"
    local file="$dir/buffer.conf"

    debug "Target file: $file"

    if [[ -f "$file" ]]; then
        echo "  $file already exists."
        if ! confirm "  Overwrite $file?"; then
            echo "[audio] Skipped."
            return
        fi
    fi

    debug "Creating directory $dir"
    mkdir -p "$dir"

    debug "Writing $file"
    cat > "$file" <<'EOF'
[pulse.properties]
pulse.min.quantum = 2048/48000
pulse.default.reg = 2048/48000
pulse.default.frag = 4096/48000
EOF

    echo "[audio] $file written."
}

