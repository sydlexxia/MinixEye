# ─────────────────────────────────────────────
# SECTION 5 — CPU: Performance governor
# Installs cpufrequtils, sets all cores to the 'performance' governor,
# and persists the setting via /etc/default/cpufrequtils.
# ─────────────────────────────────────────────
configure_cpu_governor() {
    echo ""
    echo "=== SECTION 6: CPU — Performance Governor ==="

    debug "Checking for cpufrequtils"
    if ! command -v cpufreq-set &>/dev/null; then
        echo "  cpufrequtils not found — installing..."
        debug "Running: apt-get install -y cpufrequtils"
        apt-get install -y cpufrequtils
    fi

    debug "Setting governor to performance for all CPUs"
    cpufreq-set -r -g performance
    echo "  Governor set to: performance"

    local conf="/etc/default/cpufrequtils"
    debug "Writing $conf"

    if [[ -f "$conf" ]]; then
        if grep -q '^GOVERNOR=' "$conf"; then
            debug "Updating existing GOVERNOR line in $conf"
            sed -i 's/^GOVERNOR=.*/GOVERNOR="performance"/' "$conf"
        else
            echo 'GOVERNOR="performance"' >> "$conf"
        fi
    else
        echo 'GOVERNOR="performance"' > "$conf"
    fi

    echo "[cpu] Governor persisted in $conf."
}

# ─────────────────────────────────────────────
# SECTION 6 — GPU/KERNEL: Intel P-state via GRUB
# Adds intel_pstate=active to GRUB_CMDLINE_LINUX_DEFAULT so the Intel GPU
# and CPU P-state driver runs in active (highest performance) mode.
# Runs update-grub after patching.
# ─────────────────────────────────────────────
configure_grub_pstate() {
    echo ""
    echo "=== SECTION 7: GRUB — Intel P-state ==="

    local grub_file="/etc/default/grub"
    debug "Reading $grub_file"

    if [[ ! -f "$grub_file" ]]; then
        echo "ERROR: $grub_file not found — skipping." >&2
        return
    fi

    if grep -q 'intel_pstate=active' "$grub_file"; then
        echo "  intel_pstate=active already present in $grub_file — skipping."
    else
        confirm "  Patch $grub_file to add intel_pstate=active and run update-grub?" || { echo "[grub] Skipped."; return; }

        debug "Backing up $grub_file to ${grub_file}.bak"
        cp "$grub_file" "${grub_file}.bak"

        debug "Injecting intel_pstate=active into GRUB_CMDLINE_LINUX_DEFAULT"
        # Append the parameter inside the existing quoted value
        sed -i 's/^\(GRUB_CMDLINE_LINUX_DEFAULT="[^"]*\)"/\1 intel_pstate=active"/' "$grub_file"

        echo "  Patched. Running update-grub..."
        debug "Running: update-grub"

        # update-grub failure can leave /boot/grub/grub.cfg malformed → unbootable.
        # Run inside an if-guard (bypasses set -e), restore on failure, then
        # try update-grub a second time to regenerate a known-good grub.cfg.
        if ! update-grub; then
            echo "  !! update-grub FAILED — restoring ${grub_file}.bak" >&2
            cp "${grub_file}.bak" "$grub_file"
            if ! update-grub; then
                hard_stop "update-grub failed twice" \
                    "Your grub config has been restored from backup but" \
                    "regeneration of /boot/grub/grub.cfg also failed." \
                    "DO NOT REBOOT until you have run 'sudo update-grub'" \
                    "manually and verified /boot/grub/grub.cfg is valid."
            fi
            hard_stop "update-grub failed (config rolled back)" \
                "/etc/default/grub restored; grub.cfg regenerated from backup."
        fi

        echo "[grub] Done. A reboot is required for this change to take effect."
    fi
}

# ─────────────────────────────────────────────
# SECTION 10 — HEADLESS: Disable LightDM and desktop services (RUNS LAST)
# Disables LightDM (display manager) and stops GUI-related services so the
# machine boots to a lean, headless runlevel. Motion and SSH remain active.
# ─────────────────────────────────────────────
disable_desktop() {
    echo ""
    echo "=== SECTION 10: Headless Mode — Disable Desktop Services ==="
    echo "  WARNING: This will disable the graphical desktop environment."
    echo "  The machine will boot to a text console only."
    echo ""

    # ── SSH gate: refuse to strand the operator on a headless box ───────────
    echo "  Verifying SSH is active and listening on :22 before continuing..."
    if ! verify_ssh_reachable; then
        echo "  !! SSH is NOT reachable on port 22." >&2
        echo "  !! Disabling the desktop now would leave the machine inaccessible." >&2
        echo "" >&2
        echo "  To fix: sudo apt install openssh-server && sudo systemctl enable --now ssh" >&2
        echo "" >&2
        if ! confirm "  Override the SSH gate and disable the desktop anyway?"; then
            echo "[headless] Skipped — SSH gate not satisfied."
            return 0
        fi
        echo "  !! Operator overrode SSH gate. Proceeding at your own risk."
    else
        echo "  SSH OK."
    fi

    confirm "  Disable LightDM and desktop services now?" || { echo "[headless] Skipped."; return; }

    # Set default boot target to multi-user (no GUI)
    debug "Setting default systemd target to multi-user.target"
    systemctl set-default multi-user.target

    # Disable and stop LightDM
    if systemctl list-unit-files | grep -q '^lightdm.service'; then
        debug "Disabling and stopping lightdm.service"
        systemctl disable lightdm.service
        systemctl stop lightdm.service 2>/dev/null || true
        echo "  lightdm disabled."
    else
        echo "  lightdm.service not found — skipping."
    fi

    # Disable other common desktop/display services if present
    local desktop_services=(
        "gdm.service"
        "gdm3.service"
        "sddm.service"
        "accounts-daemon.service"
        "colord.service"
        "cups.service"
        "bluetooth.service"
        "avahi-daemon.service"
    )

    for svc in "${desktop_services[@]}"; do
        if systemctl list-unit-files | grep -q "^${svc}"; then
            debug "Disabling $svc"
            systemctl disable "$svc" 2>/dev/null || true
            systemctl stop "$svc" 2>/dev/null || true
            echo "  $svc disabled."
        fi
    done

    echo "[headless] System configured for headless operation."
    echo "           Reboot to apply all changes."
}

# ─────────────────────────────────────────────
# SECTION 8 — LOGGING: Remote rsyslog and/or USB storage
#
# Forwards all syslog traffic off the eMMC to one or both destinations:
#   • Remote syslog server (rsyslog omfwd, TCP or UDP)
#   • USB-attached storage (block device → auto-mounted, rsyslog file output)
#
# Local filesystem logging (rsyslog 50-default.conf) can be disabled entirely
# once a remote/USB destination is confirmed, protecting eMMC longevity.
#
# Override any prompt by exporting these variables before running the script:
#   LOG_REMOTE_HOST     IP or hostname of the remote syslog receiver
#   LOG_REMOTE_PORT     UDP/TCP port (default: 514)
#   LOG_REMOTE_PROTO    "tcp" or "udp" (default: tcp)
#   LOG_USB_DEVICE      Block device path, e.g. /dev/sdb1
#   LOG_USB_MOUNT       Mount point (default: /mnt/usb-log)
#   LOG_DISABLE_LOCAL   Set to "true" to suppress the confirm prompt
# ─────────────────────────────────────────────
configure_logging() {
    echo ""
    echo "=== SECTION 8: Logging — Remote rsyslog / USB Storage ==="
    echo "  Goal: route all logs off the eMMC; optionally silence local writes."
    echo ""

    local remote_host="${LOG_REMOTE_HOST:-}"
    local remote_port="${LOG_REMOTE_PORT:-514}"
    local remote_proto="${LOG_REMOTE_PROTO:-tcp}"
    local usb_device="${LOG_USB_DEVICE:-}"
    local usb_mount="${LOG_USB_MOUNT:-/mnt/usb-log}"
    local disable_local="${LOG_DISABLE_LOCAL:-}"

    local do_remote=false
    local do_usb=false

    # ── 9a: Remote rsyslog forwarding ────────────────────────────────────────
    if confirm "  Configure remote rsyslog forwarding?"; then
        if [[ -z "$remote_host" ]]; then
            read -r -p "    Remote syslog host (IP or hostname): " remote_host
        fi

        if [[ -z "$remote_host" ]]; then
            echo "    No host entered — skipping remote forwarding."
        else
            do_remote=true
            echo "    Host:     $remote_host"
            echo "    Port:     $remote_port  (override: export LOG_REMOTE_PORT=<n>)"
            echo "    Protocol: $remote_proto  (override: export LOG_REMOTE_PROTO=tcp|udp)"

            # Legacy-style queue directives for broad rsyslog 8.x compatibility.
            # The in-memory queue buffers up to 10 000 messages if the remote is
            # temporarily unreachable; SaveOnShutdown is intentionally off to
            # avoid writing a queue spool file to eMMC.
            local proto_flag="@@"                       # TCP (reliable)
            [[ "$remote_proto" == "udp" ]] && proto_flag="@"

            debug "Writing /etc/rsyslog.d/10-remote-forward.conf"
            mkdir -p /etc/rsyslog.d
            cat > /etc/rsyslog.d/10-remote-forward.conf <<EOF
# Remote syslog forwarding — generated by minix_z83_postinstall.sh
\$ActionQueueType        LinkedList
\$ActionQueueSize        10000
\$ActionQueueSaveOnShutdown off
\$ActionResumeRetryCount -1

*.* ${proto_flag}${remote_host}:${remote_port}
EOF
            echo "    [logging] Remote → ${remote_proto^^} ${remote_host}:${remote_port} configured."
        fi
    fi

    # ── 9b: USB-attached storage logging ────────────────────────────────────
    if confirm "  Configure logging to USB-attached storage?"; then
        if [[ -z "$usb_device" ]]; then
            echo ""
            echo "    Detected block devices:"
            lsblk -o NAME,SIZE,TYPE,MOUNTPOINT 2>/dev/null | grep -v loop || true
            echo ""
            read -r -p "    USB device path (e.g. /dev/sdb1): " usb_device
        fi

        if [[ -z "$usb_device" ]]; then
            echo "    No device entered — skipping USB logging."
        else
            do_usb=true
            [[ ! -b "$usb_device" ]] && \
                echo "    WARNING: $usb_device is not a recognised block device. Continuing anyway."

            debug "Creating mount point $usb_mount"
            mkdir -p "$usb_mount"

            if ! grep -q "$usb_device" /etc/fstab; then
                debug "Adding $usb_device to /etc/fstab (with snapshot)"
                fstab_begin
                # nofail: boot continues cleanly if the stick is absent
                echo "${usb_device}  ${usb_mount}  auto  defaults,noatime,nofail  0  0" >> /etc/fstab
                fstab_validate_or_rollback "USB log mount ($usb_device)"
                echo "    $usb_device → $usb_mount added to fstab (nofail, validated)."
            fi

            mount "$usb_device" "$usb_mount" 2>/dev/null \
                && echo "    Mounted $usb_device at $usb_mount." \
                || echo "    (Will mount on next boot — device may not be attached right now.)"

            debug "Writing /etc/rsyslog.d/20-usb-log.conf"
            cat > /etc/rsyslog.d/20-usb-log.conf <<EOF
# USB storage logging — generated by minix_z83_postinstall.sh
*.* ${usb_mount}/syslog.log
EOF
            echo "    [logging] USB → ${usb_mount}/syslog.log configured."
        fi
    fi

    # ── 9c: Disable local filesystem logging ────────────────────────────────
    if $do_remote || $do_usb; then
        echo ""
        echo "    A remote/USB log destination is active."

        local do_disable=false
        if [[ "$disable_local" == "true" ]]; then
            do_disable=true
        elif confirm "  Disable local filesystem logging entirely (recommended for eMMC)?"; then
            do_disable=true
        fi

        if $do_disable; then
            # Neutralise the default local-file ruleset shipped with rsyslog
            if [[ -f /etc/rsyslog.d/50-default.conf ]]; then
                debug "Renaming 50-default.conf → 50-default.conf.disabled"
                mv /etc/rsyslog.d/50-default.conf \
                   /etc/rsyslog.d/50-default.conf.disabled
                echo "    50-default.conf disabled — no local /var/log writes via rsyslog."
            fi

            # Tell journald to hand off to rsyslog rather than writing its own files.
            # Amend the conf written in Section 3d if it exists; create it otherwise.
            local jconf="/etc/systemd/journald.conf.d/99-emmc.conf"
            mkdir -p "$(dirname "$jconf")"
            if [[ -f "$jconf" ]]; then
                if ! grep -q 'ForwardToSyslog' "$jconf"; then
                    debug "Appending ForwardToSyslog=yes to $jconf"
                    echo "ForwardToSyslog=yes" >> "$jconf"
                fi
            else
                cat > "$jconf" <<'EOF'
[Journal]
Storage=volatile
RuntimeMaxUse=32M
Compress=yes
ForwardToSyslog=yes
EOF
            fi
            debug "Restarting systemd-journald"
            systemctl restart systemd-journald
            echo "    journald: Storage=volatile, ForwardToSyslog=yes."
        fi
    else
        echo "    No log destinations configured — local logging unchanged."
    fi

    # ── 9d: Restart rsyslog ──────────────────────────────────────────────────
    if $do_remote || $do_usb; then
        if systemctl list-unit-files 2>/dev/null | grep -q '^rsyslog.service'; then
            debug "Restarting rsyslog"
            systemctl restart rsyslog
            echo "    [logging] rsyslog restarted."
        else
            echo "    WARNING: rsyslog not found. Add 'rsyslog' to the PACKAGES array (Section 1)."
        fi
    fi

    echo "[logging] Done."
}

# ─────────────────────────────────────────────
# SECTION 9 — MOTIONEYE: pip bootstrap + install + verify
#
# Steps:
#   10a  Bootstrap pip via get-pip.py (skipped if pip3 already present)
#   10b  Configure /etc/pip.conf with break-system-packages=true
#   10c  pip install motioneye
#   10d  motioneye_init  (creates /etc/motioneye, /var/lib/motioneye, service)
#   10e  Verify the web UI answers on port 8765
# ─────────────────────────────────────────────
install_motioneye() {
    echo ""
    echo "=== SECTION 9: motionEye Installation ==="

    # ── 10a: Bootstrap pip ───────────────────────────────────────────────────
    echo "  [10a] Checking for pip3..."
    if command -v pip3 &>/dev/null; then
        echo "        pip3 already installed — skipping bootstrap."
    else
        debug "Downloading get-pip.py"
        curl -sSfO 'https://bootstrap.pypa.io/get-pip.py'
        debug "Running: python3 get-pip.py"
        python3 get-pip.py
        rm -f get-pip.py
        echo "        pip3 installed."
    fi

    # ── 10b: Configure /etc/pip.conf ────────────────────────────────────────
    echo "  [10b] Configuring /etc/pip.conf (break-system-packages=true)..."
    local pip_conf="/etc/pip.conf"

    # Add [global] section header if it doesn't exist
    if ! grep -q '^\[global\]' "$pip_conf" 2>/dev/null; then
        debug "Adding [global] header to $pip_conf"
        printf '[global]\n' | tee -a "$pip_conf" > /dev/null
    fi

    # Add break-system-packages only if not already present
    if ! grep -q 'break-system-packages' "$pip_conf" 2>/dev/null; then
        debug "Inserting break-system-packages=true under [global]"
        sed -i '/^\[global\]/a break-system-packages=true' "$pip_conf"
        echo "        break-system-packages=true written."
    else
        echo "        break-system-packages already set — skipping."
    fi

    # ── 10c: pip install motioneye ───────────────────────────────────────────
    echo "  [10c] Installing motionEye via pip..."
    debug "Running: python3 -m pip install motioneye"
    python3 -m pip install motioneye
    echo "        motionEye installed."

    # ── 10d: motioneye_init ──────────────────────────────────────────────────
    echo "  [10d] Running motioneye_init..."
    if command -v motioneye_init &>/dev/null; then
        debug "Running: motioneye_init"
        motioneye_init
        echo "        motioneye_init complete."
    else
        echo "  ERROR: motioneye_init not found after install — check pip output above." >&2
        return 1
    fi

    # ── 10e: Verify web UI on port 8765 ──────────────────────────────────────
    echo "  [10e] Verifying motionEye web UI on port 8765..."
    local local_ip
    local_ip=$(ip route get 1.1.1.1 2>/dev/null | awk '/src/{print $7; exit}')
    [[ -z "$local_ip" ]] && local_ip=$(hostname -I 2>/dev/null | awk '{print $1}')

    local retries=10
    local delay=3
    local ok=false

    for ((i=1; i<=retries; i++)); do
        debug "Attempt $i/${retries}: curl http://${local_ip}:8765/"
        if curl -sSf --max-time 4 "http://${local_ip}:8765/" -o /dev/null 2>/dev/null; then
            ok=true
            break
        fi
        echo "        Waiting for motionEye to start... (${i}/${retries})"
        sleep "$delay"
    done

    if $ok; then
        echo ""
        echo "  ✔  motionEye web UI is up: http://${local_ip}:8765/"
    else
        echo ""
        echo "  WARNING: motionEye did not respond on http://${local_ip}:8765/ after $((retries * delay))s."
        echo "           Check: sudo systemctl status motioneye"
        echo "           Logs:  sudo journalctl -u motioneye -n 50"
    fi

    echo "[motioneye] Done."
}

