#!/usr/bin/env bash
# Post-install configuration script for Linux Mint on Minix Z83-4
# Target: headless motion surveillance system
# Usage: sudo ./minix_z83_postinstall.sh [--debug] [--help]

set -euo pipefail

# ─────────────────────────────────────────────
# Flags
# ─────────────────────────────────────────────
DEBUG=false
ASSUME_YES=false
FORCE=false
LOG_FILE=""

usage() {
    cat <<EOF
Usage: sudo $(basename "$0") [OPTIONS]

Post-install script for Linux Mint on the Minix Z83-4 mini PC.
Configures the system for headless operation as a motion surveillance node.

OPTIONS:
  -h, --help    Show this help message and exit
  -d, --debug   Print each step before executing it
  -y, --yes     Assume "yes" to all prompts (non-interactive mode).
                A single up-front summary still requires acknowledgement
                unless --force is also supplied.
      --force   Skip the up-front change summary acknowledgement.
                Combined with --yes, runs the entire script unattended.

WHAT THIS SCRIPT DOES:
  1. Installs packages from the package list (edit PACKAGES array below)
  2. Applies additional system tweaks (edit TWEAKS section below)
  3. eMMC preservation: disables swap, noatime, tmpfs mounts, volatile journal,
     log rotation, core dump suppression, write-back tuning
  4. Fixes audio buffering via a PipeWire override config
  5. Installs the Broadcom WiFi firmware for the BCM43455 adapter
  6. Sets the CPU frequency governor to 'performance'
  7. Enables Intel P-state active mode via GRUB kernel parameter
  8. Configures rsyslog to forward logs to a remote syslog host and/or USB
     storage, with the option to disable local filesystem logging entirely
  9. Installs motionEye (pip bootstrap → pip.conf → pip install → motioneye_init)
     and verifies the web UI is reachable on port 8765
 10. Disables LightDM and stops desktop/display services for headless operation
     (refuses to run if SSH is not reachable on port 22)

Must be run as root (sudo).
EOF
    exit 0
}

for arg in "$@"; do
    case "$arg" in
        -h|--help)  usage ;;
        -d|--debug) DEBUG=true ;;
        -y|--yes)   ASSUME_YES=true ;;
        --force)    FORCE=true ;;
        *) echo "Unknown option: $arg" >&2; usage ;;
    esac
done

# ─────────────────────────────────────────────
# Helpers
# ─────────────────────────────────────────────
debug() { $DEBUG && echo "[DEBUG] $*"; }

confirm() {
    local prompt="$1"
    if $ASSUME_YES; then
        echo "$prompt [y/N] y   (auto-confirmed via --yes)"
        return 0
    fi
    local response
    read -r -p "$prompt [y/N] " response
    [[ "$response" =~ ^[Yy]$ ]]
}

require_root() {
    if [[ $EUID -ne 0 ]]; then
        echo "ERROR: This script must be run as root. Use: sudo $0" >&2
        exit 1
    fi
}

# Exits the script with a formatted error banner. Args: title, line1, [line2...]
hard_stop() {
    local title="$1"; shift
    echo "" >&2
    echo "  ╔═══════════════════════════════════════════════════════════════╗" >&2
    printf  "  ║  %-61s  ║\n" "!! HARD STOP — ${title} !!" >&2
    echo "  ╠═══════════════════════════════════════════════════════════════╣" >&2
    for line in "$@"; do
        printf "  ║  %-61s  ║\n" "$line" >&2
    done
    echo "  ╚═══════════════════════════════════════════════════════════════╝" >&2
    echo "" >&2
    exit 1
}

preflight_check() {
    echo ""
    echo "--- Pre-flight: system validation ---"

    # ── Check 1: apt package lists exist and are less than 24 hours old ─────
    # /var/cache/apt/pkgcache.bin is regenerated by every successful apt update.
    local cache="/var/cache/apt/pkgcache.bin"

    if [[ ! -f "$cache" ]]; then
        hard_stop "apt update has never been run" \
            "No apt package cache found. The system must be fully" \
            "updated before this script is permitted to run." \
            "" \
            "Please run these commands and review all output carefully:" \
            "  sudo apt update" \
            "  sudo apt upgrade" \
            "" \
            "Then re-run this script."
    fi

    local cache_age=$(( $(date +%s) - $(stat -c %Y "$cache") ))
    local max_age=86400  # 24 hours

    if [[ "$cache_age" -gt "$max_age" ]]; then
        local hours=$(( cache_age / 3600 ))
        hard_stop "apt package lists are stale" \
            "The package cache is ${hours}h old (limit: 24h)." \
            "Run a fresh update/upgrade and review all output carefully:" \
            "" \
            "  sudo apt update" \
            "  sudo apt upgrade" \
            "" \
            "Then re-run this script."
    fi

    # ── Check 2: zero packages pending upgrade ───────────────────────────────
    local pending
    pending=$(apt list --upgradable 2>/dev/null | grep -c '\[upgradable' || true)

    if [[ "$pending" -gt 0 ]]; then
        hard_stop "system has ${pending} pending upgrade(s)" \
            "${pending} package(s) are waiting to be upgraded." \
            "A full upgrade must be completed and reviewed by you" \
            "before this script is permitted to run." \
            "" \
            "  sudo apt upgrade" \
            "" \
            "Review all changes, then re-run this script."
    fi

    # ── Check 3: internet connectivity (apt, pip, get-pip.py all need it) ───
    if ! ping -c 1 -W 2 1.1.1.1 >/dev/null 2>&1 \
       && ! ping -c 1 -W 2 8.8.8.8 >/dev/null 2>&1; then
        hard_stop "no internet connectivity detected" \
            "Cannot reach 1.1.1.1 or 8.8.8.8." \
            "This script needs internet to fetch apt packages, get-pip.py," \
            "and the motionEye Python package." \
            "" \
            "Verify network/DNS and re-run."
    fi

    # ── Check 4: required fstab-validation tool is present ──────────────────
    if ! command -v findmnt &>/dev/null; then
        hard_stop "findmnt is missing" \
            "fstab safety checks require 'findmnt' (util-linux)." \
            "Install it: sudo apt install util-linux"
    fi

    echo "    Package lists: fresh (updated within last 24h)"
    echo "    Pending upgrades: 0"
    echo "    Internet reachable: yes"
    echo "    fstab validator (findmnt): present"
    echo "    Pre-flight passed."
}

# ─────────────────────────────────────────────
# Safety helpers — fstab snapshot/rollback and SSH gating
# ─────────────────────────────────────────────

# Path to the most recent fstab snapshot taken by fstab_begin.
FSTAB_SNAPSHOT=""

# Take a snapshot of /etc/fstab before any edits in the current section.
fstab_begin() {
    FSTAB_SNAPSHOT="$(mktemp /tmp/fstab.snapshot.XXXXXX)"
    cp /etc/fstab "$FSTAB_SNAPSHOT"
    debug "fstab snapshot → $FSTAB_SNAPSHOT"
}

# Validate /etc/fstab via findmnt. On failure, restore the snapshot and
# hard_stop — protects the box from an unbootable fstab.
fstab_validate_or_rollback() {
    local label="${1:-fstab edit}"
    if findmnt --verify --tab-file /etc/fstab >/dev/null 2>&1; then
        debug "fstab validation passed: $label"
        rm -f "$FSTAB_SNAPSHOT"
        FSTAB_SNAPSHOT=""
        return 0
    fi

    echo "  !! fstab validation FAILED after: $label" >&2
    findmnt --verify --tab-file /etc/fstab >&2 || true

    if [[ -n "$FSTAB_SNAPSHOT" && -f "$FSTAB_SNAPSHOT" ]]; then
        cp "$FSTAB_SNAPSHOT" /etc/fstab
        echo "  !! /etc/fstab rolled back from snapshot." >&2
    fi
    hard_stop "fstab validation failed: ${label}" \
        "/etc/fstab was rolled back to its prior state." \
        "Inspect the change manually before re-running."
}

# True if sshd is active AND something is listening on port 22.
# Used as a gate before disabling the display manager so the operator
# does not lose all access to a headless machine.
verify_ssh_reachable() {
    local svc_ok=false
    if systemctl is-active --quiet ssh 2>/dev/null; then svc_ok=true; fi
    if systemctl is-active --quiet sshd 2>/dev/null; then svc_ok=true; fi
    $svc_ok || return 1

    if command -v ss &>/dev/null; then
        ss -tlnH 2>/dev/null | awk '{print $4}' | grep -qE '(^|:)22$' || return 1
    elif command -v netstat &>/dev/null; then
        netstat -tlnH 2>/dev/null | awk '{print $4}' | grep -qE '(^|:)22$' || return 1
    fi
    return 0
}

